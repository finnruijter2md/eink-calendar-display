#include <error.hpp>

Error::Error(const char *message)
{
    this->message = String(message);
}

Error::Error(const String &message)
{
    this->message = message;
}

Error::Error(const StringSumHelper &message)
{
    this->message = message.c_str();
}

ErrorWifiConnection::ErrorWifiConnection(uint status) : Error("Unable to connect to Wifi. Status: " + String(WiFi.status()))
{
    this->status = status;
}

String ErrorWifiConnection::getStatus()
{
    switch (status)
    {
    case wl_status_t::WL_IDLE_STATUS:
        return "Network driver idle";
    case wl_status_t::WL_NO_SSID_AVAIL:
        return "Network with provided SSID not found";
    case wl_status_t::WL_CONNECT_FAILED:
        return "Connection to provided network failed";
    case wl_status_t::WL_CONNECTION_LOST:
        return "Connection lost";
    case wl_status_t::WL_DISCONNECTED:
        return "Disconnected from network";
    default:
        return "Unknown error: " + String(status);
    }
}

